    private void txtProdKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProdKeyReleased
        if(this.txtProd.getText().isEmpty()){
            llenaProducto(false, "");
        }else{
            llenaProducto(true, this.txtProd.getText());
        }
    }//GEN-LAST:event_txtProdKeyReleased

    private void tblProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductoMouseClicked
        prod = new Producto();
        int fila;
        fila = this.tblProducto.getSelectedRow();
        prod.setId_producto(Integer.parseInt(dtm.getValueAt(fila, 0).toString()));
        prod.setDescripcion(dtm.getValueAt(fila, 1).toString());
        prod.setTipo(dtm.getValueAt(fila, 2).toString());
        prod.setPrecio(Double.parseDouble(dtm.getValueAt(fila, 3).toString()));
        prod.setEstado(Integer.parseInt(dtm.getValueAt(fila, 4).toString()));
        this.dispose();
    }//GEN-LAST:event_tblProductoMouseClicked
