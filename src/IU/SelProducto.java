    public void llenaProducto(boolean swr, String cad){
        Vector<Producto> listProd = prodDAO.ListaItem(swr, cad);
        if(dtm.getRowCount()>0){
            dtm.setRowCount(0);  //Este método limpia el jtable cliente
        }
        for(int i=0;i<listProd.size();i++){
            Vector vProd = new Vector();
            vProd.add(0, listProd.get(i).getId_producto());
            vProd.add(1, listProd.get(i).getDescripcion());
            vProd.add(2, listProd.get(i).getTipo());
            vProd.add(3, listProd.get(i).getPrecio());
            vProd.add(4, listProd.get(i).getEstado());
            dtm.addRow(vProd);            
        }
    }
